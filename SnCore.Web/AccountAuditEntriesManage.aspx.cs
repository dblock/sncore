public partial class AccountAuditEntriesManage : AuthenticatedPage
{
    public void Page_Load(object sender, EventArgs e)
    {
        if (!IsPostBack)
        {
            SiteMapDataAttribute sitemapdata = new SiteMapDataAttribute();
            sitemapdata.Add(new SiteMapDataAttributeNode("Me Me", Request, "AccountManage.aspx"));
            sitemapdata.Add(new SiteMapDataAttributeNode("Activity", Request.Url));
            StackSiteMap(sitemapdata);

            broadcastView.AccountId = SessionManager.AccountId;
            friendsView.AccountId = SessionManager.AccountId;
        }
    }
}
